package OperacionesBasicas;

public class OperacionesBasicas {
	
	public static double sumar(double num1,double num2) {
		double resultadoSuma=num1+num2;
		return resultadoSuma;
	}
	public static double restar(double num1,double num2) {
		double resultadoResta=num1-num2;
		return resultadoResta;				
	}	
	public static double multiplicar(double num1,double num2) {
		double resultadoMultiplicacion=num1*num2;
		return resultadoMultiplicacion;
		}	
	public static double divisor(double num1,double num2) {
		double resultadoDivisor=num1/num2;
		return resultadoDivisor;
		}	
		
	}	



